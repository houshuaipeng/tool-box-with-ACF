%――――――――――――――――――――――2――――――――――――――――――――――%
% bbApply用于在图片上绘制得到的矩形框，其调用的语法为：
% bbApply( action, varargin )//其中varargin指的是可变参数列表
% 其中action有：
%       'area'：计算矩形框的面积，参数为bb的名字
%       'shift'：转移中心，参数为bb的名字、x坐标偏移量、y坐标偏移量
%       'getCenter'：得到矩形框的中心，参数为矩形框的名字
%       'intersect'：获取两个矩形框的交集，参数;矩形框1、矩形框2
%       'union'：获取两个矩形框的并集，参数;矩形框1、矩形框2
%       'resize'：中心不变，改变矩形框大小，参数：矩形框名字、高度缩放比、宽度缩放比、目标纵横比（有一个缩放比为0时用）
%       'squarify'：调整宽高比到指定比例
%       'draw'：绘制矩形框，参数：矩形框名字、颜色（可统一可不统一）、线宽、线形、其他属性、颜色数组（颜色不统一时用）
%       'embed'：将矩形框嵌入图像，参数：图像名字、矩形框名字、属性设置
%       'crop'：裁剪检测出的目标
%       'convert'：将矩形框由相对坐标转化为绝对坐标或者反过来
%       'random'：随机生成落在指定区域的矩形框
%       'frMask'：将加权掩码转换为矩形框
%       'toMask'：创建加权掩码编码矩形框中心（或范围）。
      
